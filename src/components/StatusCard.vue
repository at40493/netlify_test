</script>

<template>
  <div class="status-card group">
    <div class="flex items-start justify-between">
      <div class="flex items-center">
        <div class="text-primary-500 dark:text-primary-400 mr-3" v-html="iconMap[icon]"></div>
        <h3 class="text-lg font-medium">{{ title }}</h3>
      </div>
      
      <button 
        v-if="copyable && !loading" 
        @click="copyToClipboard"
        class="p-1.5 text-neutral-400 hover:text-primary-500 hover:bg-neutral-100 dark:hover:bg-neutral-700 rounded-lg transition-colors duration-200"
        :class="{ 'text-success-500': copied }"
        title="Copy to clipboard"
      >
        <svg v-if="!copied" xmlns="http://www.w3.org/2000/svg" class="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
          <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M8 5H6a2 2 0 00-2 2v12a2 2 0 002 2h10a2 2 0 002-2v-1M8 5a2 2 0 002 2h2a2 2 0 002-2M8 5a2 2 0 012-2h2a2 2 0 012 2m0 0h2a2 2 0 012 2v3m2 4H10m0 0l3-3m-3 3l3 3" />
        </svg>
        <svg v-else xmlns="http://www.w3.org/2000/svg" class="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
          <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M5 13l4 4L19 7" />
