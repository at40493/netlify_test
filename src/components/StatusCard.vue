<script setup lang="ts">
import { ref } from 'vue';

const props = defineProps<{
  title: string;
  value: string;
  icon: string;
  loading?: boolean;
  copyable?: boolean;
}>();

const copied = ref(false);

function copyToClipboard() {
  if (!props.value) return;
  
  navigator.clipboard.writeText(props.value).then(() => {
    copied.value = true;
    setTimeout(() => {
      copied.value = false;
    }, 2000);
  });
}

